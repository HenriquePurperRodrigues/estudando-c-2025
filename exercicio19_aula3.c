#include <stdio.h>

/*
Num dia de sol, você deseja medir a altura de um prédio, porém, a trena não é
suficientemente longa. Assumindo que seja possível medir sua sombra e a do prédio no
chão, e que você lembre da sua altura, faça um algoritmo para ler os dados necessários
e calcular a altura do prédio.
*/

int main()
{
    float alturaPessoa, sombraPessoa, sombraPredio, alturaPredio;

    printf("Digite a sua altura: ");
    scanf("%f", &alturaPessoa);

    printf("\nDigite a largura da sua sombra: ");
    scanf("%f", &sombraPessoa);

    printf("\nDigite a largura da sombra do prédio: ");
    scanf("%f", &sombraPredio);

    alturaPredio = alturaPessoa * sombraPredio / sombraPessoa;

    printf("\nEsse prédio mede %.2f metros\n", alturaPredio);

    return 0;

}
